"""Orchestrate E1: run each of 6 MR runners against each of the 6 SUTs in the
multi-checkpoint robustness study and aggregate per-MR distributions.

SUT roster (defined in docs/superpowers/plans/2026-06-06-empirical-enhancement-E1E2E3.md):
- S0..S3 = base config (hidden=64, layers=4), training seeds {0,1,2,3} — seed-replica family;
- S4    = hidden=128, seed 0 — wide variant;
- S5    = num_layers=6, seed 0 — deep variant.

Runs all 6 MR runners per SUT, then aggregates per-MR key metrics. Bootstrap 95% CI is
reported across S0..S3 (the seed-replica family); S4 / S5 are reported descriptively
(single-instance config variants).

Honesty boundary: the K=6 checkpoints share one architecture family and one dataset; this
is a cross-checkpoint/cross-configuration robustness study, not a cross-SUT generalization
claim. The Minimum-MR-SubSet repo is read-only; all manifests and outputs land here.
"""
